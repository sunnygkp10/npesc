<div class="panel panel-primary box">
 <div class="panel-heading title1">Call For Paper</div>
 
  <div class="panel-body title2">
<p>Papers are invited for the conference in the following topics of interest that include, but are not limited to:<br /><br />


<b>Renewable Energy Technologies:</b> Sources, Storage, Grid Integration and Microgrids, smart-grid, Energy Efficiency, Green Technologies, Bio MEMS,  Wind, solar, biomass and wave energy systems, Fuel cells.<br /><br />


<b>Power Electronics and Drives:</b> Power Converters, Electric Power Quality, FACTS and Custom Power Devices, Power Filters, AC/DC Drives, Electrical drives control and applications, Switch Mode Power Supplies and UPS, Hybrid Electric Vehicles.<br /><br />

<b>Distribution Systems:</b> Reforms, Automation, Distribution, Generation and Grid Integration, Metering, Tariff, Pricing and Policy.<br /><br />

<b>Power System Analysis:</b>Operation, Control, Planning, Stability, Dynamics, and Power Systems Blackouts, Asset Management and Reliability, Demand Side Management.<br /><br />

<b>Power Markets:</b> Restructuring, Market Design, Congestion Management, Available Power Transfer Capability, Economics.<br /><br />

<b>Power System Measurement: </b>PMUs, Wide Area Measurement Systems, Information and Communication Technologies<br /><br />

<b>Power Components and Systems: </b>Machines, Transformers, Cables, Transmission and Substations.<br /><br />

<b>High Voltage Engineering:</b> HVDC and EHV Transmission, Insulation Engineering.<br /><br />

<b>Control Systems:</b> Adaptive and nonlinear control, optimal and robust control, estimation and identification, intelligent control, industrial process control, supervisory systems, digital controllers, applications in industries, Biomedical Instrumentation and their applications, Virtual Instrumentations, Robotics, Mechatronics, Hybrid Controllers.<br /><br />

<b>Soft Computing: </b>Fuzzy Computing, approximate reasoning, Neuro Computing, Learning Systems, Evolutionary Computation, Stochastic search, Nature inspired computing-Particle Swarm Optimization, Ant Colony, Tabu Search, Bacteria Foraging etc.<br /><br />
</p>
</div></div><!--closing of call for paper-->

<div class="panel panel-primary box">
 <div class="panel-heading title1">Paper Submission</div>
 
  <div class="panel-body title2">
  <p>

Full Text Manuscripts in  IEEE template should be submitted by one of the authors of the manuscript here or mail to npescknit@gmail.com . Only electronic PDF (*.pdf) file shall be considered for review. Length of the paper is limited up to 6 pages. Submissions by anyone other than one of the authors will not be accepted. All the future correspondence shall be done only with corresponding author. 
<br /><br />
<b>For submitting paper <a href="https://easychair.org/conferences/?conf=npesc2015" target="_blank">Click Here!</a></b><br /><br />
<b>NOTE:- We use EasyChair Conference Management system for online paper submission.

Authors will be required to log on to their account. New users will be required to create an account.</b>
</p></div></div><!--closing of paper submissin-->



<div class="panel panel-primary box">
 <div class="panel-heading title1">Paper Review process</div>
 
  <div class="panel-body title2">
  <p>
  Full text paper submissions to conference will be peer-reviewed by the members of the specific Technical Committee & Editorial Review Board to assess their quality and relevance to scope of conferences and the attendees of the conference. The reviewers' recommendations determine whether a paper shall be accepted / accepted subject to change / subject to resubmission with significant changes / rejected.<br /><br/>
     Conference Coordinator reserves the right to refuse any manuscript, whether on invitation or otherwise, and to make suggestions and/or modifications before publication. <br /><br />
     After provisional acceptance the corresponding author shall send the Word file of the revised manuscript to link www.npesc.in or mail to npescknit@gmail.com. <br /><br /><br />

<b>NOTE:</b> Selected quality papers shall be sent to reputed Journals in India and abroad for publication. There shall be best paper awards in various categories as well.
</p>
